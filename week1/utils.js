// ------- Addition ---------

function AddNumbers(num1, num2) {
  const res = num1 + num2;

  return res;
}

function SubstractNumbers(num1, num2) {
  const res = num1 - num2;

  return res;
}

function MultiplyNumbers(num1, num2) {
  const res = num1 * num2;

  return res;
}

function DivideNumbers(num1, num2) {
  const res = num1 / num2;

  return res;
}

export { AddNumbers, SubstractNumbers, MultiplyNumbers, DivideNumbers };
